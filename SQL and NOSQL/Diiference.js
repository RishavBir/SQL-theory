

/*

DIFFERENCE BETWEEN SQL AND NOSQL DATABASE.


SQL (Structured Query Language)  >>>> 
                
        1) It is primarily called as Relational Databases(RDBMS).
        2) It basically stores data in a tabular format.
        3) They are vertically scalable.
        4) Ex: MS-SQL, Postgres.



NoSQL (Not Only SQL) >>>>

        1) They are primarily called as Non - Relational Database.
        2) They basically stores dtata in key - value format or JSON format. They dont support tabular format.
        3) They are horizontaly scalable. 
        4) Ex: Mongodb, Redis etc.


*/